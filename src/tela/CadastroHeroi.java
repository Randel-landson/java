    public void tbLista() {
        DefaultTableModel mode = (DefaultTableModel) tb.getModel();
        mode.setNumRows(0);

        QueryHeroi h = new QueryHeroi();
        for (Heroi novo : h.Mostra()) {
            mode.addRow(new Object[]{
                novo.getCod(),
                novo.getNome(),
                novo.getPode(),});

        }
    }

