    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        // TODO add your handling code here:
        if (tb.getSelectedRow() != -1) {
            DefaultTableModel mode = (DefaultTableModel) tb.getModel();
            cod.setText(tb.getValueAt(tb.getSelectedRow(), 0).toString());
            nome.setText(tb.getValueAt(tb.getSelectedRow(), 1).toString());
            pode.setText(tb.getValueAt(tb.getSelectedRow(), 2).toString());

        }

    }//GEN-LAST:event_tbMouseClicked

    private void tbKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbKeyReleased
        // TODO add your handling code here:
        if (tb.getSelectedRow() != -1) {
            DefaultTableModel mode = (DefaultTableModel) tb.getModel();

            cod.setText(tb.getValueAt(tb.getSelectedRow(), 0).toString());
            nome.setText(tb.getValueAt(tb.getSelectedRow(), 1).toString());
            pode.setText(tb.getValueAt(tb.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tbKeyReleased

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        Heroi h = new Heroi();

        h.setCod(Integer.parseInt(cod.getText()));

        QueryHeroi del = new QueryHeroi();

        del.Delete(h);

        cod.setText(null);
        nome.setText(null);
        pode.setText(null);

        tbLista();


    }//GEN-LAST:event_delActionPerformed

    private void atalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atalizarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        if (tb.getSelectedRow() != -1) {

            

            Heroi h = new Heroi();

            h.setNome(nome.getText());
            
            h.setPode(pode.getText());
            
            h.setCod((int)tb.getValueAt(tb.getSelectedRow(),0));
            
            QueryHeroi atz = new QueryHeroi();

            atz.altera(h);

            cod.setText(null);
            nome.setText(null);
            pode.setText(null);

            tbLista();

        }
    }//GEN-LAST:event_atalizarActionPerformed

